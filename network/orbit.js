    let transformLinkData = function(apiData){
      let allLinks = apiData.links;
      let allNodes = apiData.nodes;
      let finalLinkArr = []
      for(let i=0; i < allLinks.length; i++){
        let curLink = allLinks[i];
        let sourceInd = _.findIndex(allNodes, function(o) { return o.id == curLink.source; });
        let targetInd = _.findIndex(allNodes, function(o) { return o.id == curLink.target; });
        let souTar = {
          "source" : sourceInd,
          "target" : targetInd
        }
        finalLinkArr.push(souTar);
      }
      apiData.links = finalLinkArr;
      return apiData;
    }
